#include<bits/stdc++.h>
using namespace std;
using ll = long long;

const int N = 3e5+5;

ll arr[N];
ll frr[N];
int n;



void solve()
{
    scanf("%d",&n);
    for(int i=0;i<n;i++)
    {
        scanf("%lld %lld",&arr[i],frr[i]);
    }

}

int main()
{
    int q;
    scanf("%d",&q);
    while(q--)solve();
}
